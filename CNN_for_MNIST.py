with torch.no_grad():
  for images,labels in testloader:
    outputs=model(images)
    _,predicted=torch.max(outputs,1)

    correct_labels+=(predicted==labels).sum().item()
    total_labels+=labels.size(0)

print(f"accuracy={correct_labels/total_labels*100}")

